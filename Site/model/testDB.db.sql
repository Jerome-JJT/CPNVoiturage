BEGIN TRANSACTION;
DROP TABLE IF EXISTS "CITIES";
CREATE TABLE IF NOT EXISTS "CITIES" (
	"id"	INTEGER NOT NULL,
	"name"	VARCHAR(50) NOT NULL DEFAULT '',
	PRIMARY KEY("id")
);
DROP TABLE IF EXISTS "EDT";
CREATE TABLE IF NOT EXISTS "EDT" (
	"id"	INTEGER NOT NULL,
